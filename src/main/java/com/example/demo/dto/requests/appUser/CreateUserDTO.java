package com.example.demo.dto.requests.appUser;

import com.example.demo.interfaces.DtoInterface;

public class CreateUserDTO implements DtoInterface {

    private String username;
    private String password;
    private String email;

    public CreateUserDTO(String username, String password, String email) {
        this.username = username;
        this.password = password;
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
